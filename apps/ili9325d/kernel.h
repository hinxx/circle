#include <circle/gpiopin.h>
#include <circle/types.h>

enum TShutdownMode
{
	ShutdownNone,
	ShutdownHalt,
	ShutdownReboot
};

class CKernel
{
public:
	CKernel (void);
	~CKernel (void);

	boolean Initialize (void);

	TShutdownMode Run (void);

    void Write_Command(unsigned int c);
    void Write_Data(unsigned int c);
    void Write_Command_Data(unsigned cmd, unsigned dat);
    void Lcd_Init(void);
    void SetXY(unsigned x0, unsigned x1, unsigned y0, unsigned y1);
    void Paint(unsigned color);
    void LCD_clear(void);

private:
	// do not change this order
    CMemorySystem		m_Memory;
	CActLED			m_ActLED;
	CKernelOptions		m_Options;
	CDeviceNameService	m_DeviceNameService;
	CSerialDevice		m_Serial;
	CExceptionHandler	m_ExceptionHandler;
	CInterruptSystem	m_Interrupt;
	CTimer			m_Timer;
	CLogger			m_Logger;

    CGPIOPin m_DB[8];
    CGPIOPin m_nWR;
    CGPIOPin m_nCS;
    CGPIOPin m_RS;
    CGPIOPin m_RST;
};

#endif
